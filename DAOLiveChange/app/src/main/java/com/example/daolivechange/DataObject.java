package com.example.daolivechange;

public class DataObject {
    private int apple;
    private int banana;
    private int orange;

    public DataObject() {
    }

    public int getApple() {
        return apple;
    }

    public int getBanana() {
        return banana;
    }

    public int getOrange() {
        return orange;
    }

    public void setApple(int apple) {
        this.apple = apple;
    }

    public void setBanana(int banana) {
        this.banana = banana;
    }

    public void setOrange(int orange) {
        this.orange = orange;
    }
}
