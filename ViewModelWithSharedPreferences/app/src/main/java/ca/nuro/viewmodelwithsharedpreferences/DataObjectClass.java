package ca.nuro.viewmodelwithsharedpreferences;

public class DataObjectClass {
    private int fishes;
    private float animals;
    DataObjectClass(){

    }

    public int getFishes() {
        return fishes;
    }

    public void setFishes(int fishes) {
        this.fishes = fishes;
    }

    public float getAnimals() {
        return animals;
    }

    public void setAnimals(float animals) {
        this.animals = animals;
    }
}
